is_running() {
    [ -f "$PID_FILE" ] && kill -0 "$(cat "$PID_FILE")" 2>/dev/null
}

clear_stale_pid() {
    if [ -f "$PID_FILE" ] && ! kill -0 "$(cat "$PID_FILE")" 2>/dev/null; then
        rm -f "$PID_FILE"
    fi
}

wait_for_url() {
    local max_wait=${1:-15}
    local waited=0
    while [ "$waited" -lt "$max_wait" ]; do
        if command -v curl >/dev/null 2>&1 && curl -fsS "$URL" >/dev/null 2>&1; then
            return 0
        fi
        sleep 1
        waited=$((waited + 1))
    done
    return 1
}

start_server() {
    clear_stale_pid
if is_running; then
        log "Already running (PID $(cat "$PID_FILE"))"
        return 0
    fi

    if [ -z "${START_COMMAND:-}" ] || { [ ! "${START_COMMAND:0:1}" = "(" ] && [ ! -x "${START_COMMAND%% *}" ] && ! command -v "${START_COMMAND%% *}" >/dev/null 2>&1; }; then
        if [ -z "${START_COMMAND:-}" ]; then
            gui_error "No startup command found" \
"$APP_DISPLAY is installed, but I don't know how to start it. Check the
[runtime].startup-command-search or [runtime].command in burble.launcher.a2ml."
            return 1
        fi
    fi

    log "Starting $APP_DISPLAY..."
    cd "$REPO_DIR" 2>/dev/null || true
nohup "${START_COMMAND[@]}" >"$LOG_FILE" 2>&1 &
    echo $! > "$PID_FILE"

    sleep 0.2
    if ! kill -0 "$(cat "$PID_FILE")" 2>/dev/null; then
        gui_error "Process exited immediately" "Check $LOG_FILE"
        rm -f "$PID_FILE"
        return 1
    fi

if ! wait_for_url "$WAIT_SECONDS"; then
        gui_error "Server did not start" \
"$APP_DISPLAY did not become reachable at $URL within $WAIT_SECONDS seconds.
Check $LOG_FILE — and try: curl -v $URL"
        if command -v feedback-o-tron >/dev/null 2>&1; then
            feedback-o-tron --event "launcher:start_failed" \
                --app "$APP_NAME" --url "$URL" --log "$LOG_FILE" \
                --error "Timeout after $WAIT_SECONDS seconds" 2>/dev/null || true
        fi
        return 1
    fi
    log "Server started (PID $(cat "$PID_FILE")) — $URL"
    return 0
}

stop_server() {
if ! is_running; then
        log "No running instance found"
        return 0
    fi
    log "Stopping $APP_DISPLAY..."
    kill "$(cat "$PID_FILE")" 2>/dev/null || true
    rm -f "$PID_FILE"
    log "Stopped"
}

open_browser() {
    if [ -z "$URL" ]; then
        log "No URL configured for $APP_DISPLAY"
        return 1
    fi
if ! is_running; then
        err "Server is not running — use --start or --auto first"
        return 1
    fi
    log "Opening $URL..."
    case "$PLATFORM" in
        linux)
            if   command -v xdg-open >/dev/null 2>&1; then xdg-open "$URL" &
            elif command -v firefox  >/dev/null 2>&1; then firefox  "$URL" &
            elif command -v chromium >/dev/null 2>&1; then chromium "$URL" &
            else warn "No browser found — open manually: $URL"; fi ;;
        macos)   open "$URL" 2>/dev/null || warn "Open manually: $URL" ;;
        windows)
            if   command -v start    >/dev/null 2>&1; then start    "$URL"
            elif command -v cygstart >/dev/null 2>&1; then cygstart "$URL"
            else warn "Open manually: $URL"; fi ;;
        *) warn "Unknown platform — open manually: $URL" ;;
    esac
}

